package java04_control.condition;

public class If_01 {

	public static void main(String[] args) {
		
		if(true) {
			System.out.println("zz");			
		} // 조건식이 참일때 실행할 코드를 작성한다.
		
		if(false) {
			System.out.println("ss");
		}
		
		System.out.println("if문 바깥쪽 출력");
		
		
	}

}

